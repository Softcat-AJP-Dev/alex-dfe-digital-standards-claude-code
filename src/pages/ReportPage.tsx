import { useEffect, useMemo, useState } from "react";
import { useParams } from "react-router-dom";
import { api, type ReportSnapshot } from "../api";
import { RadarChart } from "../components/RadarChart";

const LEVEL_NAMES = ["", "Initial", "Developing", "Compliant", "Established", "Leading"];

export function ReportPage() {
  const { id } = useParams<{ id: string }>();
  const [snapshot, setSnapshot] = useState<ReportSnapshot | null>(null);
  const [generatedAt, setGeneratedAt] = useState<string | null>(null);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    if (!id) return;
    api
      .getReport(id)
      .then((r) => {
        setSnapshot(r.snapshot);
        setGeneratedAt(r.generated_at);
      })
      .catch((err) => setError(String(err)));
  }, [id]);

  const radarPoints = useMemo(
    () =>
      (snapshot?.standards ?? []).map((s) => ({
        label: s.title,
        value: s.averageLevel,
      })),
    [snapshot],
  );

  const prioritised = useMemo(() => {
    if (!snapshot) return [];
    const rows: {
      standardTitle: string;
      slug: string;
      code: string;
      text: string;
      level: number | null;
      weight: number;
      standardWeight: number;
      priority: number;
      govUkUrl: string;
    }[] = [];
    for (const s of snapshot.standards) {
      for (const sc of s.subCriteria) {
        if (sc.level == null || sc.notApplicable) continue;
        const gap = 5 - sc.level;
        rows.push({
          standardTitle: s.title,
          slug: s.slug,
          code: sc.code,
          text: sc.text,
          level: sc.level,
          weight: sc.weight,
          standardWeight: s.weight,
          priority: gap * sc.weight * s.weight,
          govUkUrl: s.govUkUrl,
        });
      }
    }
    return rows.sort((a, b) => b.priority - a.priority).slice(0, 15);
  }, [snapshot]);

  if (error) return <p className="text-red-700">{error}</p>;
  if (!snapshot) return <p className="text-slate-500 text-sm">Loading…</p>;

  return (
    <article className="bg-white rounded shadow-sm p-6 print:shadow-none print:p-0">
      <header className="no-print flex items-center justify-between mb-6">
        <h1 className="text-2xl font-semibold">Maturity report</h1>
        <button
          onClick={() => window.print()}
          className="bg-slate-900 text-white px-3 py-1.5 rounded text-sm"
        >
          Print / save as PDF
        </button>
      </header>

      <section className="mb-6">
        <h2 className="text-xl font-semibold">{snapshot.assessmentTitle}</h2>
        <p className="text-sm text-slate-500">
          Generated by {snapshot.generatedByEmail} on{" "}
          {generatedAt ? new Date(generatedAt).toLocaleString("en-GB") : "—"}
        </p>
      </section>

      <section className="grid grid-cols-1 md:grid-cols-2 gap-6 mb-6">
        <div>
          <h3 className="font-semibold mb-2">Overall maturity</h3>
          <p className="text-5xl font-light">{snapshot.overallScore.toFixed(2)} / 5</p>
          <p className="text-sm text-slate-500 mt-1">
            {LEVEL_NAMES[Math.round(snapshot.overallScore)] || ""}
            <span className="block text-xs mt-1">
              Weighted across all answered, in-scope sub-criteria.
            </span>
          </p>
        </div>
        <div>
          <h3 className="font-semibold mb-2">Maturity by standard</h3>
          <RadarChart points={radarPoints} />
        </div>
      </section>

      <section className="mb-6 page-break">
        <h3 className="font-semibold mb-2">Top priorities</h3>
        <p className="text-xs text-slate-500 mb-2">
          Lowest score × sub-criterion weight × standard weight — biggest levers first.
        </p>
        <table className="w-full text-sm">
          <thead className="text-left text-xs text-slate-500 border-b">
            <tr>
              <th className="py-1">Standard</th>
              <th>Code</th>
              <th>Sub-criterion</th>
              <th className="text-right">Current</th>
            </tr>
          </thead>
          <tbody>
            {prioritised.map((p) => (
              <tr key={`${p.slug}-${p.code}`} className="border-b align-top">
                <td className="py-2 pr-2">{p.standardTitle}</td>
                <td className="py-2 pr-2 font-mono text-xs">{p.code}</td>
                <td className="py-2 pr-2">
                  {p.text}{" "}
                  <a
                    href={p.govUkUrl}
                    target="_blank"
                    rel="noreferrer"
                    className="text-xs text-sky-700 underline no-print"
                  >
                    source
                  </a>
                </td>
                <td className="py-2 text-right whitespace-nowrap">
                  <span
                    className={`inline-block px-2 py-0.5 rounded text-white text-xs ${
                      p.level === 1
                        ? "bg-level1"
                        : p.level === 2
                        ? "bg-level2"
                        : p.level === 3
                        ? "bg-level3"
                        : p.level === 4
                        ? "bg-level4"
                        : "bg-level5"
                    }`}
                  >
                    L{p.level} {LEVEL_NAMES[p.level ?? 0]}
                  </span>
                </td>
              </tr>
            ))}
          </tbody>
        </table>
      </section>

      <section className="page-break">
        <h3 className="font-semibold mb-2">Standard-by-standard breakdown</h3>
        {snapshot.standards.map((s) => (
          <div key={s.slug} className="mb-6">
            <h4 className="font-semibold text-base">
              {s.title}{" "}
              <span className="text-xs text-slate-500 font-normal">
                ({s.category}, weight ×{s.weight})
                {s.averageLevel != null && ` · L${s.averageLevel.toFixed(1)}`}
              </span>
            </h4>
            <ul className="mt-2 space-y-1">
              {s.subCriteria.map((sc) => (
                <li key={sc.code} className="text-sm border-b py-1">
                  <span className="font-mono text-xs text-slate-500">{sc.code}</span>{" "}
                  {sc.text}
                  <div className="text-xs mt-1">
                    {sc.notApplicable ? (
                      <span className="text-slate-500">Not applicable</span>
                    ) : sc.level == null ? (
                      <span className="text-slate-400">Not answered</span>
                    ) : (
                      <>
                        <span className="font-medium">L{sc.level} {LEVEL_NAMES[sc.level]}</span>
                        {sc.rationale && <span className="text-slate-600"> — {sc.rationale}</span>}
                      </>
                    )}
                  </div>
                </li>
              ))}
            </ul>
          </div>
        ))}
      </section>
    </article>
  );
}
